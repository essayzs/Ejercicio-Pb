public class ejercicio217 {
    public static void main(String[] args) {
        // Iterar sobre los primeros 9 números naturales
        for (int i = 1; i <= 9; i++) {
            // Calcular el cuadrado de cada número e imprimirlo
            int cuadrado = i * i;
            System.out.println("El cuadrado de " + i + " es: " + cuadrado);
        }
    }
}
