public class ejercicio124 {
    public static void main(String[] args) {
        // Iterar a través de los números del 1 al 3000
        for (int i = 1; i <= 3000; i++) {
            // Verificar si el número es un múltiplo de 3
            if (i % 3 == 0) {
                System.out.println(i); // Imprimir el múltiplo de 3
            }
        }
    }
}
